package cmd

import (
	"context"
	"fmt"
	"log/slog"
	"os"
	"os/signal"
	"syscall"

	"github.com/monkenwu/selfmd/internal/claude"
	"github.com/monkenwu/selfmd/internal/config"
	"github.com/monkenwu/selfmd/internal/generator"
	"github.com/monkenwu/selfmd/internal/git"
	"github.com/monkenwu/selfmd/internal/scanner"
	"github.com/spf13/cobra"
)

var sinceCommit string

var updateCmd = &cobra.Command{
	Use:   "update",
	Short: "Incremental update based on git changes",
	Long: `Analyze git changes and incrementally update affected documentation pages.
Requires initial documentation generated by selfmd generate.`,
	RunE: runUpdate,
}

func init() {
	updateCmd.Flags().StringVar(&sinceCommit, "since", "", "compare with specified commit (default: last generate/update commit)")
	rootCmd.AddCommand(updateCmd)
}

func runUpdate(cmd *cobra.Command, args []string) error {
	if err := claude.CheckAvailable(); err != nil {
		return err
	}

	cfg, err := config.Load(cfgFile)
	if err != nil {
		return err
	}

	rootDir, err := os.Getwd()
	if err != nil {
		return err
	}

	if !git.IsGitRepo(rootDir) {
		return fmt.Errorf("%s", "current directory is not a git repository, cannot perform incremental update")
	}

	level := slog.LevelInfo
	if verbose {
		level = slog.LevelDebug
	}
	logger := slog.New(slog.NewTextHandler(os.Stderr, &slog.HandlerOptions{Level: level}))

	ctx, cancel := signal.NotifyContext(context.Background(), syscall.SIGINT, syscall.SIGTERM)
	defer cancel()

	gen, err := generator.NewGenerator(cfg, rootDir, logger)
	if err != nil {
		return err
	}

	// Determine comparison commit
	previousCommit := sinceCommit
	if previousCommit == "" {
		// Try reading saved commit from last generate/update
		saved, readErr := gen.Writer.ReadLastCommit()
		if readErr == nil && saved != "" {
			previousCommit = saved
		} else {
			// Fallback to merge-base
			base, err := git.GetMergeBase(rootDir, cfg.Git.BaseBranch)
			if err != nil {
				return fmt.Errorf("cannot get base commit: %w\nhint: run selfmd generate first or use --since to specify a commit", err)
			}
			previousCommit = base
		}
	}

	currentCommit, err := git.GetCurrentCommit(rootDir)
	if err != nil {
		return fmt.Errorf("cannot get current commit: %w", err)
	}

	changedFiles, err := git.GetChangedFiles(rootDir, previousCommit, currentCommit)
	if err != nil {
		return err
	}

	changedFiles = git.FilterChangedFiles(changedFiles, cfg.Targets.Include, cfg.Targets.Exclude)

	if changedFiles == "" {
		fmt.Println("No file changes detected.")
		return nil
	}

	fmt.Printf("Comparing: %s..%s\n", previousCommit[:8], currentCommit[:8])
	fmt.Printf("Changed files:\n%s\n\n", changedFiles)

	// Scan project structure (needed for content generation)
	scan, err := scanner.Scan(cfg, rootDir)
	if err != nil {
		return fmt.Errorf("failed to scan project: %w", err)
	}

	return gen.Update(ctx, scan, previousCommit, currentCommit, changedFiles)
}
